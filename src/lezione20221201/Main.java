package lezione20221201;

public class Main {
    public static void main(String[] args) {
        System.out.println("Hello world!");
    }
}

/*
public vuol dire che è codice pubblico, visibile da tutti all'interno ed all'esterno (mi serve per aprire l'eseguibile dall'esterno)
class è l'idea che noi andremo a realizzare
lezione20221201.Main è il nome della funzione che andremo ad eseguire

public è uguale a prima
static vuol dire che questa funzionalità è uguale per tutte le classi. L'opposto è final
void significa che non mi deve restituire alcun valore
main è soltanto un nome

String[] args è un vettore che ci indica i dati che ci vengono dall'esterno, e le parentesi quadre indicano che non sappiamo ancora quanti valori necessiteremo
System.out.println() serve per stampare i dati
    System è l'interfaccia di comunicazione di Java
    .out è un oggetto dentro all'oggetto
    .println è la funzionalità che viene richamata



 */